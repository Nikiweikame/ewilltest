<script setup>
import { onMounted, ref } from "vue";

const paddleSwitch = ref(true);
function animatePaddle() {
  const paddle = document.querySelector(".header__paddle");
  if (paddleSwitch.value) {
    paddleSwitch.value = !paddleSwitch.value;
    paddle.style.transform = "rotate(-10deg)";
  } else {
    paddleSwitch.value = !paddleSwitch.value;
    paddle.style.transform = "rotate(10deg)";
  }
}
function animateBobble(bobble, opacity, Xplus, x, y, size) {
  if (Math.abs(x) === size) {
    Xplus = !Xplus;
  }
  if (Xplus) {
    ++x;
  } else {
    --x;
  }
  --y;
  // console.log(opacity);
  if (opacity === 0) {
    bobble.style.opacity = 0;
    setTimeout(() => {
      bobble.style.transform = `translate(0px, 0px)`;
    }, 3000);
    setTimeout(() => {
      animateBobble(bobble, 200, Xplus, 0, 0, size);
      bobble.style.opacity = 1;
    }, 6000);
    // return;
  }
  if (opacity === -20) {
    return;
  }
  opacity = opacity - 1;
  bobble.style.transform = `translate(${x}px, ${y}px)`;
  // bobble.style.opacity = `${opacity / 100}`;
  setTimeout(() => {
    animateBobble(bobble, opacity, Xplus, x, y, size);
  }, 100);
}
onMounted(() => {
  animatePaddle();
  const bigBbobble = document.querySelector(".header__big-bobble");
  const smallBbobble = document.querySelector(".header__small-bobble");
  animateBobble(bigBbobble, 200, false, 0, 0, 20);
  setTimeout(() => {
    animateBobble(smallBbobble, 180, true, 0, 0, 12);
  }, 500);
  setInterval(animatePaddle, 5000);
});
